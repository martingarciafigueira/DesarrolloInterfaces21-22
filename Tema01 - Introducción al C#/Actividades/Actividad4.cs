


Para expresar de manera más detallada la expresión : "Masajear el cabello hasta limpiar el champú"

Podríamos poner: "Continuar masajeando el cabello hasta que esté completamente enjuagado"

¿La diferencia? En la segunda dejamos claro que es una sentencia de repetición, mientras que en la primera podría ser una sentencia que se realiza una vez y finaliza.